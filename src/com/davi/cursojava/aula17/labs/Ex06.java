package com.davi.cursojava.aula17.labs;

public class Ex06 {

	public static void main(String[] args) {
		
		System.out.println("Com loop for e \\n");
		for (int i = 1; i <= 20; i++) {
			System.out.println(i);
		}
		
		System.out.println("Com loop while side by side");
		int i = 1;
		while (i <= 20) {
			System.out.print(i + " ");
			i++;
		}
	}

}
